export function getAllTaunts() {
  let returnObject = [];
  for (const char of AllTaunts) {
    for (const taunt of char.taunt_slugs) {
      returnObject.push(taunt)
    }
  }
  return returnObject;
}

export function getTauntsByChar(charSlug: string) {
  const char = AllTaunts.find((c) => c.character_slug === charSlug);
  return char ? char.taunt_slugs : [];
}
